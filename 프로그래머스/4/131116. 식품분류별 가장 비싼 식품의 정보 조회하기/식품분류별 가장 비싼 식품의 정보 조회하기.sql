WITH MAX_PRICE_BY_CATEGORY AS (
    SELECT CATEGORY, MAX(PRICE) MAX_PRICE
    FROM FOOD_PRODUCT
    WHERE CATEGORY IN ('과자', '국', '김치', '식용유')
    GROUP BY CATEGORY
)
SELECT A.CATEGORY, A.MAX_PRICE, B.PRODUCT_NAME
FROM MAX_PRICE_BY_CATEGORY A
JOIN FOOD_PRODUCT B ON A.CATEGORY=B.CATEGORY AND A.MAX_PRICE=B.PRICE
ORDER BY A.MAX_PRICE DESC;